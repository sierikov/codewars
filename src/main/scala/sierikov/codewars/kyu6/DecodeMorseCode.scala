package sierikov.codewars.kyu6

import scala.collection.immutable.HashMap

object DecodeMorseCode {

  // order and codes are given by kata
  object MorseCodes {

    val morseCodes: Map[String, String] = HashMap(
      "---..." -> ":",
      "...-..-" -> "$",
      "...." -> "H",
      ".-." -> "R",
      ".-.-." -> "+",
      "-...." -> "6",
      ".--." -> "P",
      "-.--.-" -> ")",
      "..--.." -> "?",
      "-.-." -> "C",
      "--.-" -> "Q",
      "..-." -> "F",
      ".-..." -> "&",
      ".--" -> "W",
      "---.." -> "8",
      "..-" -> "U",
      "-." -> "N",
      "-----" -> "0",
      "-....-" -> "-",
      "." -> "E",
      "--..--" -> ",",
      "-..-" -> "X",
      ".----" -> "1",
      "..--.-" -> "_",
      "--." -> "G",
      "-.--." -> "(",
      "-..-." -> "/",
      ".----." -> "'",
      "...-" -> "V",
      "-" -> "T",
      ".--.-." -> "@",
      "---" -> "O",
      ".-" -> "A",
      "....-" -> "4",
      "-.-.-." -> ";",
      "-.." -> "D",
      ".." -> "I",
      ".-..-." -> "\"",
      "-.--" -> "Y",
      "--.." -> "Z",
      "...--" -> "3",
      "..." -> "S",
      "....." -> "5",
      "----." -> "9",
      "--" -> "M",
      ".-.-.-" -> ".",
      "..---" -> "2",
      "...---..." -> "SOS",
      "-.-" -> "K",
      ".-.." -> "L",
      ".---" -> "J",
      "-...-" -> "=",
      "-..." -> "B",
      "--..." -> "7",
      "-.-.--" -> "!"
    )
  }

  import MorseCodes.morseCodes

  def decode(msg: String): String =
    msg.trim
      .split(" {3}")
      .map(_.split(' ').map(morseCodes).mkString)
      .mkString(" ")

}
